/** All terms, sorted alphabetically by `term` (locale-aware). */
export const glossaryTerms: GlossaryTerm[] = [...terms].sort((a, b) =>
  a.term.localeCompare(b.term),
)

/** Resolve a deep-link key (an id or a term label) to a glossary entry. */
export function findTerm(key: string | undefined): GlossaryTerm | undefined {
  if (!key) return undefined
  const k = key.toLowerCase()
  return glossaryTerms.find(
    (t) => t.id === k || t.term.toLowerCase() === k,
  )
}
