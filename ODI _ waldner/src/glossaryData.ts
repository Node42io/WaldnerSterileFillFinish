// Glossary data — the single source of truth for the report's terminology.
// Each entry is one dictionary term: an `id` (used for deep-linking from the
// inline "book-open" buttons), the visible `term`, an optional short gloss
// shown in the list, the full `definition`, and any external `sources`.
//
// `id`s match what `slugify` produces for the term, so an inline trigger can
// pass either the id or the term text and still resolve the right entry.
//
// User-facing copy carries no theorist or author names — frameworks are named
// neutrally (e.g. "Outcome-Driven Innovation (ODI)").

